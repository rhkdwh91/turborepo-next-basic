import type { Metadata } from "next";
import { ReactNode } from "react";
import { SpeedInsights } from "@vercel/speed-insights/next";
import ReactQueryProvider from "@/shared/ui/providers/ReactQueryProvider";
import SessionProvider from "@/shared/ui/providers/SessionProvider";
import ToastProvider from "@repo/ui/providers/ToastProvider";
import ThemeProvider from "@/shared/ui/providers/ThemeProvider";
import Header from "shared/ui/Header";
import Footer from "shared/ui/Footer";
import { auth } from "@/auth.config";

import "@repo/tailwind-config/globals.css";

export const metadata: Metadata = {
  title: "Cho's BLOG",
  description: "Generated by create turbo",
};

export default async function RootLayout({
  children,
}: {
  children: ReactNode;
}) {
  const session = await auth();
  return (
    <html>
      <body className="font-PretendardRegular">
        <ReactQueryProvider>
          <SessionProvider>
            <ThemeProvider
              attribute="class"
              defaultTheme="dark"
              enableSystem
              disableTransitionOnChange
            >
              <ToastProvider>
                <Header session={session} />
                {children}
                <Footer />
              </ToastProvider>
            </ThemeProvider>
          </SessionProvider>
        </ReactQueryProvider>
        <SpeedInsights />
        <div id={"modal-root"} />
      </body>
    </html>
  );
}
